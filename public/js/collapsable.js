'use strict';

$('#1content').hide();

$('#1header').click(function(event){
  let content =  $('#1content');
  if (content.css('display') == 'none'){
    content.show();
    $('#1icon').attr('src', '../img/minus.png');
  } else {
    content.hide();
    $('#1icon').attr('src', '../img/plus.png');
  }
});


